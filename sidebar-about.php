                    <h2>What Is Inclusive Design?</h2>                    
                    <p>Inclusive design enables and supports the participation of individuals and groups representing the full range of human diversity.</p>
                    <p>Using agile development, participatory and user-centred design practices, IDI researches, designs, and develops technology that enables matching the needs and preferences of each individual user.</p>
